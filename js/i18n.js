/* Global i18n helper */
function t(key) {
  const lang = localStorage.getItem('gg-lang') || 'zh';
  return I18N[lang]?.[key] || I18N['zh'][key] || key;
}

/* Set all elements with data-i18n attribute */
function applyTranslations(lang) {
  localStorage.setItem('gg-lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (I18N[lang]?.[key]) {
      if (el.tagName === 'INPUT' && el.type === 'text') {
        el.placeholder = I18N[lang][key];
      } else if (el.tagName === 'META') {
        el.content = I18N[lang][key];
      } else {
        el.textContent = I18N[lang][key];
      }
    }
  });

  // Update lang switch button text
  const langBtns = document.querySelectorAll('[data-lang-switch]');
  langBtns.forEach(btn => {
    btn.textContent = I18N[lang]?.switchLang || (lang === 'zh' ? 'EN' : '中');
  });

  // Update HTML title if it has a data-i18n key
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (I18N[lang]?.[key]) el.title = I18N[lang][key];
  });

  // Trigger custom event for dynamic content
  document.dispatchEvent(new CustomEvent('langChange', { detail: { lang } }));
}

/* Initialize language on page load */
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('gg-lang') || 'zh';
  applyTranslations(savedLang);

  // Language switch buttons
  document.querySelectorAll('[data-lang-switch]').forEach(btn => {
    btn.addEventListener('click', () => {
      const current = localStorage.getItem('gg-lang') || 'zh';
      const next = current === 'zh' ? 'en' : 'zh';
      applyTranslations(next);
    });
  });
});
